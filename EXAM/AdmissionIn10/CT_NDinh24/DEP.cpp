ll Find(int l, int r)
{

    ll max_sum = a[l], cur_sum = a[l];
 
    for (int i = l + 1; i <= r; i++) {
        if (cur_sum < 0) {
            cur_sum = a[i];
        } else {
            cur_sum += a[i];
        }
 
        if (cur_sum > max_sum) {
            max_sum = cur_sum;
        }
    }
    return max_sum;
}
