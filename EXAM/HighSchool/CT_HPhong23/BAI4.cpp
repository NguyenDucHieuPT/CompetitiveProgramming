map <int, int> a;
int n, k;
bool Check(int x)
{
	if (k % 2 == 0 || k <= x)
		return false;
	int y = k - x;
	return (a[y] ? true : false);
}
void query()
{
	a.clear();
	cin >> n >> k;
	int res = 0;
	while(n--)
	{
		int Ai; cin >> Ai;
		a[Ai]++;
		res = (Check(Ai) ? 1 : res);
	}
	cout << res;
}
